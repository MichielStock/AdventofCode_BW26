# ╔═╡ 1824c9c5-a274-4ff4-9870-13ea9606211c
const openings = ['(', '[', '{', '<']

# ╔═╡ 0a338a2a-b193-4241-8f2e-c64666d57f7c
const closings = [')', ']', '}', '>']

# ╔═╡ 80ce1ad2-ec83-4a25-bfc7-dd0467a01963
const matching = Dict(zip(openings, closings))

# ╔═╡ e69110e8-b3a4-45bb-b13d-a37d8b074eff
const costs = Dict(zip(closings, [3, 57, 1197, 25137]))

# ╔═╡ 0920c38f-c8fb-4c7d-a76b-8b92321a5127
const completion_cost = Dict(c=>i for (i, c) in enumerate(openings))

# ╔═╡ 29fc4232-fa33-4d30-8db7-3673ed647bf7
function iscorrupted(string)
	open = Char[]
	for c in string
		if c in openings
			push!(open, c)
		else
			if matching[last(open)] == c
				pop!(open)
			else
				return costs[c]
			end
		end
	end
	return 0
end


# ╔═╡ 77beb065-6a91-4188-ac2f-04ddceacac5d
function complete(string)
	open = Char[]
	for c in string
		if c in openings
			push!(open, c)
		else
			if matching[last(open)] == c
				pop!(open)
			else
				return 0
			end
		end
	end
	cost = 0
	while !isempty(open)
		c = pop!(open)
		cost *= 5
		cost += completion_cost[c]
	end
	return cost
end

